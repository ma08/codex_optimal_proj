

# 開始地点から終了地点への最短経路を求める
# 1. 開始地点から終了地点への経路を全探索する
# 2. 各経路で、最も回数が多くなる場所を求める
# 3. 最も回数が多くなる場所を起点に、再帰的に最短経路を探索する
# 4. 最短経路が見つかったら、それを出力する

ans = 10**10
for i in range(1 << N):
    # 各書籍について、ビットが立っていると購入する、立っていないと購入しない
    # この時、各アルゴリズムについて、購入した書籍の内、各アルゴリズムのレベルが最も高いものを足し合わせる
    # これがX以上になればOK
    cost = 0
    level = [0] * M
    for j in range(N):
        # 購入する書籍
        if i & (1 << j):
            cost += C[j]
            for k in range(M):
                level[k] = max(level[k], A[j][k])
    # すべてのアルゴリズムのレベルがX以上であれば、コストの最小値を更新する
    if all(x >= X for x in level):
        ans = min(ans, cost)

# すべてのアルゴリズムのレベルがX以上になれば、コストが最小値になる
if ans == 10**10:
    print(-1)
else:
    print(ans)
