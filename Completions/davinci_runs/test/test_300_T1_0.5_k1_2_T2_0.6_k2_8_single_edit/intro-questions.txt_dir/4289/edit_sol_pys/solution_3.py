# === main ========================================================================================================================================================
def main():
    n = int(input())
    t, a = map(int, input().split())
    h = list(map(int, input().split()))

    dif = [abs(t - h[i] * 0.006 - a) for i in range(n)] # 積雪深度が大きいときに0.006にならないように数値を調整する
    ans = dif.index(min(dif)) + 1
    print(ans)

# ==================================================================================
if __name__ == "__main__":
    main()
