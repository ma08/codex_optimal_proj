def main():
    n = input()[::-1]
    n = int(n)
    l = len(str(n))
    moves = 0
    s = 0
    for i in range(l):
        s += n % 10
        n = n // 10
        if s % 25 == 0:
            moves += i + 1
            s = 0
    if s % 25 == 0:
        moves += l
        s = 0
    if s == 0:
        print(moves)
        return
    print(-1)

if __name__ == '__main__':
    main()




