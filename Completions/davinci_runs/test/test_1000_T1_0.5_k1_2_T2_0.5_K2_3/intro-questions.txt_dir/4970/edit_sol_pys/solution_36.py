

from collections import Counter

n = int(input())  # number of course combinations

courses_combinations = []

for i in range(n):
    courses = list(map(int, input().split(" ")))  # list of courses
    courses_combinations.append(tuple(sorted(courses)))  # sort the list of courses and turn into tuple

c = Counter(courses_combinations)  # count the number of times a course combination appears
most_common = c.most_common(1)  # find the most common course combination

print(most_common[0][1])  # print the number of times the most common course combination appears
































































































































































































































































































































































































































































































































































































































































































































































































































































































