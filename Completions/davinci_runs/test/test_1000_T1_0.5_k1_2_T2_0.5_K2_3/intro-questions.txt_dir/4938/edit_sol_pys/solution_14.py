# Задача на программирование: выборы президента
# В выборах президента набрало N голосов. Напишите программу, которая определяет победителя выборов.
#
# Sample Input:
#
# 2
# 2
# 2
# Sample Output:
#
# 1

N = int(input())

votes = [0, 0]

for i in range(N):
    vote = int(input())
    votes[vote - 1] += 1

if votes[0] > votes[1]:
    print(1)
else:
    print(2)

