
N = int(input())

a = []
b = []

for i in range(N):
    a.append(int(input()))

for i in range(N):
    b.append(int(input()))

count = 0

for i in range(N):
    if a[i] > b[i]:
        count += 1

print(count)































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































red = []
blue = []

for i in range(N):
    x, y = map(int, input().split())
    red.append((x, y))

for i in range(N):
    x, y = map(int, input().split())
    blue.append((x, y))

# red.sort(key=lambda x: x[0])
red.sort(key=lambda x: x[1], reverse=True)
blue.sort(key=lambda x: x[0])
blue.sort(key=lambda x: x[1], reverse=True)

count = 0
for r in red:
    for b in blue:
        if r[0] < b[0] and r[1] < b[1]:
            count += 1
            blue.remove(b)
            break

print(count)
