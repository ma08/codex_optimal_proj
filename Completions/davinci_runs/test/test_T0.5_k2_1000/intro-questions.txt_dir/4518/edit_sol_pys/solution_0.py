
def add(a,b):
    return a+b
def sub(a,b):
    return a-b
def mul(a,b):
    return a*b
def div(a,b):
    return a/b
def mod(a,b):
    return a%b
def exp(a,b):
    return a**b

def main():
    a=int(input())
    b=int(input())
    print(add(a,b))
    print(sub(a,b))
    print(mul(a,b))
    print(div(a,b))
    print(mod(a,b))
    print(exp(a,b))

main()
