# 1. A, B, Cをそれぞれ一回ずつ使う方法を考える
# 2. A, B, Cが全て1円のとき、50円を使う方法は何通りあるか
# 3. A, B, Cが全て10円のとき、50円を使う方法は何通りあるか
# 4. A, B, Cが全て100円のとき、50円を使う方法は何通りあるか
# 5. A, B, Cが全て500円のとき、50円を使う方法は何通りあるか
# 6. A, B, Cが全てi円のとき、50円を使う方法は何通りあるか
# 7. A, B, Cが全て1円のとき、100円を使う方法は何通りあるか
# 8. A, B, Cが全て10円のとき、100円を使う方法は何通りあるか
# 9. A, B, Cが全て100円のとき、100円を使う方法は何通りあるか
# 10. A, B, Cが全て500円のとき、100円を使う方法は何通りあるか
# 11. A, B, Cが全てi円のとき、100円を使う方法は何通りあるか

# 1. まずは全探索
# 2. 全探索を再帰関数で書く
# 3. 全探索をfor文で書く
# 4. 全探索を3重ループfor文で書く

# 入力
A = int(input())
B = int(input())
C = int(input())
X = int(input())

# 初期値
ans = 0

# 全探索
for a in range(A + 1):
    for b in range(B + 1):
        for c in range(C + 1):
            if 500 * a + 100 * b + 50 * c == X:
                ans += 1

# 出力
print(ans)




























































































































































































































































































































































































































































































































































































































































































N, X = map(int, input().split())
L = list(map(int, input().split()))

cnt = 1
dist = 0
for i in range(N):
    dist += L[i]
    if dist <= X:
        cnt += 1
    else:
        break
print(cnt)
