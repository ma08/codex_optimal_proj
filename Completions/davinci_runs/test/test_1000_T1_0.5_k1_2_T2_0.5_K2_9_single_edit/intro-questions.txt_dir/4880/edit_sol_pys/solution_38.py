

import sys

def main():
    """
    Main function.
    """
    # Read the first line
    line1 = sys.stdin.readline()
    # Read the second line
    line2 = sys.stdin.readline()
    # Read the third line
    line3 = sys.stdin.readline()
    # Print the result.
    print(line1 + line2 + line3)

if __name__ == '__main__':
    main()
