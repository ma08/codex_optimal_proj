# === main =========================================================================================================================================================
def main():
    N = int(input())
    A = list(map(int, input().split()))

    sum_A = sum(A)
    ans = 0
    for i in range(N):
        if i == 0:
            ans += sum_A - A[i]
        else:
            ans -= A[i]
    print(ans)

# =====================================================================================================================================================================
if __name__ == "__main__":
    main()
