#!/usr/bin/env python
# -*- coding: utf-8 -*-

def main():
    n, x, y = map(int, input().split())
    n = list(input())

    if n[y] == '0':
        n[y] = '1'
    else:
        n[y] = '0'
        for i in range(y+1, x):
            if n[i] == '0':
                n[i] = '1'
                break
            else:
                n[i] = '0'

    print(n.count('0'))

if __name__ == '__main__':
    try:
        main()
    except KeyboardInterrupt:
        pass
    except ValueError:
        print('Invalid input')
    except EOFError:
        print('\nBye')
    except:
        import traceback
        traceback.print_exc()
        print('Unexpected error')
        raise

    try:
        input('Press Enter to continue')
    except:
        pass

    print('\nBye')


