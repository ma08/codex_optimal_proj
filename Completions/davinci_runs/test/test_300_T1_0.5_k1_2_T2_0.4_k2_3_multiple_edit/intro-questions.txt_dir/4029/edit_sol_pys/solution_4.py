def main():
    n = int(input())
    if n == 0:
        print(10)
    elif n == 1:
        print(1)
    else:
        dp = [0] * (n + 1)
        dp[0] = 1
        for i in range(1, n + 1):
            for j in range(i, n + 1):
                dp[j] += dp[j - i]
        print(dp[n])

if __name__ == '__main__':
    main()
