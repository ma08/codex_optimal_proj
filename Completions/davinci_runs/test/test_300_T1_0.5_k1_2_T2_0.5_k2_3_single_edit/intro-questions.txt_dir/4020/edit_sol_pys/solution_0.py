

h1, m1 = map(int, input().split(':')) # разделяем ввод на часы и минуты
h2, m2 = map(int, input().split(':')) # разделяем ввод на часы и минуты

m3 = (m1 + m2) // 2 # средняя минута
h3 = h1 + (m1 + m2) // 120 # средний час

if h3 > 23: # если средний час больше 23, то отнимаем 24
    h3 -= 24 # отнимаем 24

print('{:02}:{:02}'.format(h3, m3))
