

# Solution 1
N, K = map(int, input().split())
A = sorted(map(int, input().split()))

cur = 0
ans = 0

for i in range(N):
    if i > 0 and A[i] - A[i-1] > 5:
        ans = max(ans, cur)
        cur = 0
    cur += 1

ans = max(ans, cur)
print(ans)


