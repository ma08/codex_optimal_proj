

# python3
# X, K, D = map(int, input().split())
X, K, D = 6, 2, 4

# 正の整数Kに対して、KをDで割った商とあまりを求める
# 例：K=5, D=3 のとき、商は1、あまりは2
q, r = divmod(K, D)

# 前述の通り、KをDで割った商が偶数のときは、絶対値が小さくなるようにK回移動することができる
# 例：K=5, D=3 のとき、移動回数は5回で絶対値が小さくなるように移動することができる
if q % 2 == 0:
    print(abs(X - r))

# 前述の通り、KをDで割った商が奇数のときは、絶対値が小さくなるようにK回移動することができない
# 例：K=6, D=3 のとき、移動回数は6回で絶対値が小さくなるように移動することができない
else:
    # このとき、K=6, D=3 のときは、K=5, D=3 のときと同様に移動することができる
    # つまり、K=6, D=3 のときは、(K-1)回移動してから、(D-r)回移動することで絶対値が小さくなるように移動することができる
    # 例：K=6, D=3 のとき、(K-1)回移動してから、(D-r)回移動することで絶対値が小さくなるように移動することができる
    # したがって、K=6, D=3 のときの絶対値は、(D-r)となる
    print(abs(X - (D - r)))