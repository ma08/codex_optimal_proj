
n = int(input())
d = [int(x) for x in input().split()]

x = d[0]  # первый элемент массива
y = d[0]  # первый элемент массива

for i in range(1, n):
    if d[i] % x == 0:  # если остаток от деления элемента массива на x равен нулю
        y *= d[i]
    else:  # если остаток от деления элемента массива на x не равен нулю
        x *= d[i]

print(x, y)
