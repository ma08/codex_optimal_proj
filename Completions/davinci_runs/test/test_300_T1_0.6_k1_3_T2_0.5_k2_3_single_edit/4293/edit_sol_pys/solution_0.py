


import numpy as np

# 行列の入力
N, M = map(int, input().split())
A = np.zeros((N, M))
for i in range(N):
    A[i] = np.array(input().split(), dtype=np.int)

# 列ごとの合計
sum_A = np.sum(A, axis=0)

# 出力
for i in range(N):
    print(np.sum(A[i]) - sum_A[i])















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































