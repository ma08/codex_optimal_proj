def solve(n, b):
    primes = convert_to_primes(n)
    a = []
    for i, val in enumerate(b):
        if i % 2 == 0:
            a.append(val)
        else:
            if val in primes:
                a.append(primes.index(val) + 1)
            else:
                a.append(val)
    return a

